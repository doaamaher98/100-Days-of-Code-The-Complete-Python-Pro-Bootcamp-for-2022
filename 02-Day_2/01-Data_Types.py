# Primitive Data Types

# 1- Strings
print ("Hello"[0])  # prints H
# This is called subscripting.

# 2- Integers
print (1+2)         # prints 3

# 3- Float
print (1324.32)     

# 4- Boolean
## True or False

# Adding a 2-digit number code
num = input ("Enter a random 2-digit number :\n")

# num is a str => print (type(num)) -> str

# Sum them
sum = int(num[0]) + int(num[1])

# Print result
print(sum)